class Solution {
    int findMaxSum(int arr[], int n) {
        // code here
        int temp = arr[0];
        int temp2 = 0;
        for (int i=1; i<n; i++) {
            int t1 = temp;
            int t2 = temp2 + arr[i];
            int max = Math.max(t1, t2);
            temp2=  temp;
            temp = max;
        }
        return temp;
    }
}